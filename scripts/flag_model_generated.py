#!/usr/bin/env python3
"""Flag test cases whose pr_body contains model-generated signatures.

Scans all YAML files in cases/final/ and adds 'model-generated-pr-body'
to quality_flags for any case whose pr_body matches known AI-tool
signatures (Claude Code, Cursor, Copilot coding agent, etc.).

Usage:
    python scripts/flag_model_generated.py [--dry-run]
"""

from __future__ import annotations

import re
import sys
from pathlib import Path

# Add project root to path so we can import bugeval
sys.path.insert(0, str(Path(__file__).resolve().parent.parent / "src"))

from bugeval.io import load_case, save_case  # noqa: E402

FLAG = "model-generated-pr-body"

# Patterns that indicate a PR body was generated by an AI coding tool.
# These match the signature lines tools append, NOT mentions of tools as topics.
PATTERNS: list[re.Pattern[str]] = [
    re.compile(r"Generated with \[Claude Code\]", re.IGNORECASE),
    re.compile(r"Generated with \[Cursor\]", re.IGNORECASE),
    re.compile(r"Generated with \[Copilot\]", re.IGNORECASE),
    re.compile(r"Made with \[Cursor\]", re.IGNORECASE),
    re.compile(r"Made with \[Claude Code\]", re.IGNORECASE),
    re.compile(r"COPILOT CODING AGENT", re.IGNORECASE),
    re.compile(r"Copilot coding agent tips", re.IGNORECASE),
]


def matches_model_signature(pr_body: str) -> str | None:
    """Return the matched pattern description, or None."""
    for pattern in PATTERNS:
        if pattern.search(pr_body):
            return pattern.pattern
    return None


def main() -> None:
    dry_run = "--dry-run" in sys.argv

    cases_dir = Path(__file__).resolve().parent.parent / "cases" / "final"
    if not cases_dir.exists():
        print(f"ERROR: cases dir not found: {cases_dir}", file=sys.stderr)
        sys.exit(1)

    yaml_files = sorted(cases_dir.rglob("*.yaml"))
    print(f"Scanning {len(yaml_files)} YAML files in {cases_dir}\n")

    flagged: list[str] = []
    already_flagged: list[str] = []
    errors: list[str] = []

    for path in yaml_files:
        try:
            case = load_case(path)
        except Exception as e:
            errors.append(f"  ERROR loading {path.name}: {e}")
            continue

        match = matches_model_signature(case.pr_body)
        if match is None:
            continue

        if FLAG in case.quality_flags:
            already_flagged.append(case.id)
            continue

        flagged.append(case.id)
        if not dry_run:
            case.quality_flags.append(FLAG)
            save_case(case, path)

    # Report
    print(f"Matched {len(flagged) + len(already_flagged)} cases total:\n")

    if flagged:
        action = "Would flag" if dry_run else "Flagged"
        print(f"  {action} ({len(flagged)}):")
        for cid in flagged:
            print(f"    - {cid}")

    if already_flagged:
        print(f"\n  Already flagged ({len(already_flagged)}):")
        for cid in already_flagged:
            print(f"    - {cid}")

    if errors:
        print(f"\n  Errors ({len(errors)}):")
        for e in errors:
            print(e)

    if not flagged and not already_flagged:
        print("  No cases matched model-generated patterns.")

    if dry_run and flagged:
        print(f"\nDry run — no files modified. Re-run without --dry-run to apply.")


if __name__ == "__main__":
    main()
